for x in range(10):     # Iterate for loop
    if x % 20 == 0:     # Condition 1
        print("twist")

    elif x % 15 == 0:   # Condition 2
        pass            # Pass statement

    elif x % 5 == 0:    # Condition 3
        print("fizz")   # Display result

    elif x % 3 == 0:    # Condition 4
        print("buzz")   # Display result

    else:               # Condition 5
        print(x)        # Display result